      },
      child: MaterialApp.router(
        routeInformationParser: AppRouteInformationParser(),
        routerDelegate: routerDelegate,
        title: 'Flutter Demo',
        theme: ThemeData(primarySwatch: Colors.blue),
      ),
    );
  }
}
